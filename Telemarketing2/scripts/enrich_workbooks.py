def append_unique_rows(workbook_path: Path, sheet_name: str, rows: list[dict[str, str]]) -> tuple[int, int]:
    wb = load_workbook(workbook_path)
    ws = wb[sheet_name]
    headers = [cell.value for cell in ws[1]]
    header_index = {str(value).strip(): idx + 1 for idx, value in enumerate(headers) if value is not None}
    existing = set()
    for row_idx in range(2, ws.max_row + 1):
        values = []
        for header in headers:
            col = header_index.get(str(header).strip())
            values.append(str(ws.cell(row=row_idx, column=col).value or "").strip() if col else "")
        existing.add(tuple(values))

    appended = 0
    for row in rows:
        tuple_value = tuple(str(row.get(str(header).strip(), "")).strip() for header in headers)
        if tuple_value in existing:
            continue
        ws.append([row.get(str(header).strip(), "") for header in headers])
        existing.add(tuple_value)
        appended += 1

    wb.save(workbook_path)
    return appended, len(rows)


